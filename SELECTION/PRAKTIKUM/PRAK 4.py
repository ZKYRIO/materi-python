print("Program Untuk Menentukan Grade Nilai")
print("=======================================")
nilai = int(input("Masukkan Nilai : "))
if (90 < nilai <= 100):
    print("Grade A, Perfect")
elif (74 < nilai < 90):
    print("Grade B, Excellent")
elif (60 <= nilai < 74):
    print("Grade C, Good Job")
else:
    print("Grade D, You Can Do it")

print("\n")
